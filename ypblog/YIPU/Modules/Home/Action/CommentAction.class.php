class CommentAction extends CommonAction {
	
    public function review(){
		if(''===trim($_POST['verify'])){
			$this->error('请填写验证码!');
		}elseif($_SESSION['verify'] != md5($_POST['verify'])){
            $this->error('验证码错误');
        }else{
			$this->insert('评论成功，审核后可见...','评论失败');
		}
    }	


}
